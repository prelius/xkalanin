    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Statement stm;
        int us_id = -1, game_id = -1;
        try {
            stm = con.createStatement();
            String sql = "SELECT id FROM \"public\".\"users\" WHERE NAME LIKE '"+ user +"';";

            ResultSet rs = stm.executeQuery(sql);

            if(rs.next()){
                us_id = rs.getInt("id");
            }
            
            sql = "SELECT id FROM \"public\".\"gamelist\" WHERE NAME LIKE '"+ game +"';";
            rs = stm.executeQuery(sql);
            if(rs.next()){
                game_id = rs.getInt("id");
            }
            
            sql = "SELECT lib.* FROM \"public\".library AS lib LEFT JOIN \"public\".users AS us ON lib.user_id = us.id  WHERE game_id = "+game_id + " AND user_id = "+us_id + "LIMIT 1";
            rs = stm.executeQuery(sql);
            
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "Túto hru už vlastníte!", "Cyba nákupu", JOptionPane.WARNING_MESSAGE);
            }else            
            if(game_id != -1 && us_id != -1){
                sql = "INSERT INTO \"public\".library (user_id, game_id, \"date\") \n" +
                      "	VALUES ("+us_id+", "+ game_id +", current_date);";
                stm.executeUpdate(sql);
                con.commit();
                JOptionPane.showMessageDialog(this, "Hra bola úspešne kúpena. Nájdete je vo svojej knižnici hier.", "Nákup", JOptionPane.INFORMATION_MESSAGE);
            } 
            stm.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

