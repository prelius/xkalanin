    private DefaultListModel populateModel(int style, String name, int rating) {
        DefaultListModel mdl = new DefaultListModel();
        
        try {
            Statement stm = con.createStatement();
            String sql = "WITH temp_table AS (SELECT games.id, games.\"name\", games.genre, coalesce(sum(com.rating),0) / count(*) AS rate FROM \"public\".\"gamelist\" AS games FULL JOIN \"public\".\"comments\" AS com ON games.id = com.user_id GROUP BY games.\"id\") SELECT * FROM temp_table";
            sql += " WHERE \"genre\" LIKE "+ getStyle(style);
            sql += " and \"name\" LIKE "+ name;
            sql += " and \"rate\" >= " +rating;
            sql += " ORDER BY \"name\";";
            System.out.println(sql);
            ResultSet rs = stm.executeQuery(sql);
            
            while(rs.next()){
                mdl.addElement(rs.getString("name"));
            }
            rs.close();
            stm.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
        return mdl;
    }
    
