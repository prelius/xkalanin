    private void RegButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegButtonActionPerformed
        try {
            // TODO add your handling code here:
            Statement stm = con.createStatement();
            String sql = "INSERT INTO \"public\".\"users\" (name, pass, type)"
                    + " VALUES ('" + LoginField.getText()  + "', '"
                    + Arrays.toString(PassField.getPassword()) + "', 1);";
            stm.executeUpdate(sql);
            stm.close();
            con.commit();
        } catch (SQLException ex) {
            Logger.getLogger(Login_frame.class.getName()).log(Level.SEVERE, null, ex);
            try {
                con.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(Login_frame.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
        
    }//GEN-LAST:event_RegButtonActionPerformed

