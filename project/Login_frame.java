    private void LogButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogButtonActionPerformed
        // TODO add your handling code here:
        if((!LoginField.getText().isEmpty()) && 
                (PassField.getPassword().length > 0))
        {
            
            try {
                Statement stm = con.createStatement();
                String sql = "SELECT * FROM \"public\".\"users\" WHERE name like '"+ LoginField.getText() +"';";
                ResultSet rs = stm.executeQuery(sql);
                if(rs.next()) {
                    String pss = Arrays.toString(PassField.getPassword());
                    if(rs.getString("pass").equals(Arrays.toString(PassField.getPassword()))) {
                        System.out.println("Prihlásený ako " + rs.getString("name"));
                        window.setName(LoginField.getText());
                        window.setEnabled(true);
                        window.setVisible(true);
                        
                        if(rs.getString("name").equals("admin")){
                            window.enableAdmin();
                        }
                        
                        this.setEnabled(false);
                        this.setVisible(false);
                    }
                    else {
                        //System.out.println("Zadali ste zle heslo");
                        JOptionPane.showMessageDialog(this, "Zadali ste zle heslo!", "Chyba prihlasenia", JOptionPane.WARNING_MESSAGE);
                    }
                }
                else
                    //System.out.println("Pouzivatel neexistuje");
                    JOptionPane.showMessageDialog(this, "Pouzivatel neexistuje!", "Chyba prihlasenia", JOptionPane.WARNING_MESSAGE);
                stm.close();
                con.commit();
            } catch (SQLException ex) {
                Logger.getLogger(Login_frame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else {
            System.out.println("Nezadali ste meno alebo heslo");
            JOptionPane.showMessageDialog(this, "Nezadali ste meno alebo heslo", "Chyba prihlasenia", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_LogButtonActionPerformed

